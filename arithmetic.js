// Arithmetic Operators

console.log(10+20); // Addition

console.log(20-5); // Subtraction

console.log(20*2); // Multiplication

console.log(20/2); // Division

console.log(19%2); // Modulus (Remainder)

console.log(3**2); // Exponentiation (Power)

// Increment ++

let num = 3;
num++; // Post Increment // Increment by 1
++num; // Pre Increment  // Another way to increment by 1

console.log(num);

// Decrement --

let num1 = 5;
num1--; // Post Decrement // Decrement by 1
--num1; // Pre Decrement  // Another way to decrement by 1

console.log(num1);


