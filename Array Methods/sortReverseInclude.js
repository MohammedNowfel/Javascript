// Sort method

let arr = [1,2,3,4,5,11,22,33,44,55,111,222,333,444,555];
// sort using ASCII values
arr.sort(); // it will sort the array in ascending order by default (as strings)

console.log(arr);


// Reverse method

let arr1 = [1,3,5,7,13,25,47];

arr1.reverse(); // it will reverse the array

console.log(arr1);


// Include method

let arr2 = [1,3,5,4,6,9];

console.log(arr2.includes(5)); // it will return true because 5 is present in the array
console.log(arr2.includes(7)); // it will return false because 7 is not present in the array



