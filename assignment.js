// Assignment Operators

let age = 19;

age += 1; // age = age + 1  // 20
console.log(age);

age -= 2; // age = age - 2  // 18
console.log(age);

age *= 3; // age = age * 3  // 54
console.log(age);

age /= 2; // age = age / 2  // 27
console.log(age);

age %= 2; // age = age % 2  // 1 (Remainder of 27 divided by 2)
console.log(age);

age **= 2; // age = age ** 2  // 1 (1 raised to the power of 2 is still 1)
console.log(age);

