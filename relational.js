// Relational Operators in JavaScript

console.log(5 < 10); // Less than
console.log(10 <= 10); // Less than or equal to 

console.log(5 > 10); // Greater than
console.log(10 >= 10); // Greater than or equal to

console.log(5 == "5"); // Equal to
console.log(5 === "5"); // Strict equal to

console.log(5 != "5"); // Not equal to
console.log(5 !== "5"); // Strict not equal to






